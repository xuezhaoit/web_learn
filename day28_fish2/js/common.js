function rdInt(x,y){
    Math.random()
    // 0-1*5+1
    let num = Math.floor(Math.random()*(y-x)+x)
    // console.log(num);
    
    return num
}
function rdXY(x,y) {
    Math.random()
    // 0-1*5+1
    let num = Math.random()*(y-x)+x
    // console.log(num);
    
    return num
}
